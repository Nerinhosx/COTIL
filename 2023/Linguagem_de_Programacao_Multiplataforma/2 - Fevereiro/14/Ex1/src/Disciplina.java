public class Disciplina {
    private String nome;
    private String prof;
    private int cH;

    public void setNome(String nome)
    {
        this.nome = nome;
    }

    public String getNome()
    {
        return nome;
    }

    public void setProf(String prof)
    {
        this.prof = prof;
    }

    public String getProf()
    {
        return prof;
    }

    public void setCH(int cH)
    {
        this.cH = cH;
    }

    public int getCH()
    {
        return cH;
    }
}
