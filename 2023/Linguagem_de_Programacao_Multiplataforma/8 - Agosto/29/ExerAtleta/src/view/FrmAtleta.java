    private void btnCadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadActionPerformed
        if(ac.verificaExiste(Integer.parseInt(txtCod.getText()))==false)
        {    
            try{
                if((txtCod.getText().equals("")) || (txtNome.getText().equals("")) || (txtPeso.getText().equals("")) || (txtAlt.getText().equals("")) || (txtIdade.getText().equals("")))
                {
                    throw new StringVaziaException();
                }
                int cod = Integer.parseInt(txtCod.getText());
                String nome = txtNome.getText();
                double peso = Double.parseDouble(txtPeso.getText());
                double alt = Double.parseDouble(txtAlt.getText());
                int idade = Integer.parseInt(txtIdade.getText());
                if((cod<=0) || (peso<=0) || (alt<=0) || (idade<=0))
                {
                    throw new NumeroNegativoException();
                }
                ac.cadAtleta(cod, nome, peso, alt, idade);
                JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
                txtCod.setText("");
                txtNome.setText("");
                txtPeso.setText("");
                txtAlt.setText("");
                txtIdade.setText("");
            }
            catch (StringVaziaException e){
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
            catch (NumberFormatException e){
                JOptionPane.showMessageDialog(null, "Valores númericos devem ser informados nos campos numéricos.");
            }
            catch (NumeroNegativoException e){
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "O código informado já está em uso!\nPor favor, informe outro.");
            txtCod.setText("");
            txtCod.requestFocus();
        }
    }//GEN-LAST:event_btnCadActionPerformed

