    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int resp = JOptionPane.showConfirmDialog(null, "Deseja mesmo excluir?");
        if(resp == 0)
        {
            try{
                if(txtCodExcluir.getText().equals(""))
                {
                    throw new StringVaziaException();
                }
                else
                {
                    if(ac.excluir(Integer.parseInt(txtCodExcluir.getText())))
                    {
                        JOptionPane.showMessageDialog(null, "Atleta excuído com sucesso!");
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(null, "Falha na exclusão: atleta não identificado.");
                    }
                }
            }
            catch (StringVaziaException e)
            {
                JOptionPane.showMessageDialog(null, "Campo para exclusão por código está vazio!");
            }
        }
        txtCodExcluir.setText("");
    }//GEN-LAST:event_btnExcluirActionPerformed

